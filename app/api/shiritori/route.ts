// 末尾の文字を取得（「ー」対応）
const getEndChar = (word: string) => {
  let lastChar = word.slice(-1);
  if (lastChar === 'ー' && word.length > 1) {
    lastChar = word.slice(-2, -1);
  }
  return lastChar;
};

// 【新規追加】ゲーム開始時・リセット時に最初の単語をランダムに取得するAPI
export async function GET() {
  // 「ん」で終わらない単語だけを抽出
  const validStartWords = MINNA_NO_NIHONGO_WORDS.filter(
    (word) => word.slice(-1) !== 'ん'
  );

  // 候補の中からランダムに1つ選択
  const randomWord = validStartWords[Math.floor(Math.random() * validStartWords.length)];

  return NextResponse.json({ word: randomWord });
}

// プレイヤーの送信を受け取るAPI
export async function POST(request: Request) {
  try {
    const { currentWord, history } = await request.json();

    // バリデーション1: 『みんなの日本語』に含まれているか
    if (!MINNA_NO_NIHONGO_WORDS.includes(currentWord)) {
      return NextResponse.json({ success: false, message: `実在する単語を使ってね！` });
    }

    // バリデーション2: 最初の文字の不一致
    const lastWord = history[history.length - 1];
    const lastChar = getEndChar(lastWord);
    if (currentWord[0] !== lastChar) {
      return NextResponse.json({ success: false, message: `前の単語は「${lastChar}」で終わっています！` });
    }

    // バリデーション3: 単語の重複
    if (history.includes(currentWord)) {
      return NextResponse.json({ success: false, message: `「${currentWord}」はすでに使われています！` });
    }

    // バリデーション4: 「ん」で終了
    if (currentWord.slice(-1) === 'ん') {
      return NextResponse.json({ 
        success: true, 
        isGameOver: true, 
        message: '「ん」がついたので、あなたの負けです！',
        updatedHistory: [...history, currentWord]
      });
    }

    // プレイヤーのワードを履歴に追加
    const newHistory = [...history, currentWord];

    // --- AIのターン ---
    const aiFirstChar = getEndChar(currentWord);
    const availableWords = MINNA_NO_NIHONGO_WORDS.filter(word => 
      word[0] === aiFirstChar && !newHistory.includes(word)
    );

    // AIが返せる単語がない場合
    if (availableWords.length === 0) {
      return NextResponse.json({
        success: true,
        isGameOver: true,
        message: 'AI「参りました！もう返せる単語がありません」 あなたの勝ちです！',
        updatedHistory: newHistory
      });
    }

    // ランダムにAIの単語を選択
    const aiWord = availableWords[Math.floor(Math.random() * availableWords.length)];
    const nextHistory = [...newHistory, aiWord];

    // AIが「ん」で自爆した場合
    if (aiWord.slice(-1) === 'ん') {
      return NextResponse.json({
        success: true,
        isGameOver: true,
        message: `AIは「${aiWord}」と言いました。「ん」がついたのでAIの負けです！`,
        updatedHistory: nextHistory
      });
    }

    // 通常の継続
    const aiEndChar = getEndChar(aiWord);
    return NextResponse.json({
      success: true,
      isGameOver: false,
      message: `AIは「${aiWord}」と言いました。「${aiEndChar}」からどうぞ！`,
      updatedHistory: nextHistory
    });

  } catch (error) {
    return NextResponse.json({ success: false, message: 'サーバーエラーが発生しました。' }, { status: 500 });
  }
}